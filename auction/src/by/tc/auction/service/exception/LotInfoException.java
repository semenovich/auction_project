package by.tc.auction.service.exception;

public class LotInfoException extends Exception {

	private static final long serialVersionUID = 1L;

	public LotInfoException() {
		super();
	}

	public LotInfoException(String arg0) {
		super(arg0);
	}

	public LotInfoException(Throwable arg0) {
		super(arg0);
	}

	public LotInfoException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public LotInfoException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
